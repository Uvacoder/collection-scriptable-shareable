<script context="module">
   export function load(error){
      let status = error.status
      return {props:{status}}
   }
</script>

<script>
   export let status;
</script>

{#if status == 500}
   <div class="loader_error"></div>
   <p class="not_found">Something went wrong!</p>
{:else if status == 404}
   <div class="loader_error"></div>
   <p class="not_found">Nothing found here!</p>
{/if}

